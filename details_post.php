						<p>Its 300-plus members began the festivities with a “vintage” service in June using the 1867 liturgy. The congregation threw a birthday party Aug. 14, the date in 1869 of its recognition by, and reception into, the N.C. Synod.

						<p>The church began in 1869 in the area that later became Mayfair Mills, under the leadership of Gideon L. Greeson, a teacher in Company Shops. The Synod was meeting at that time at Friedens Lutheran Church north of Gibsonville, and Greeson, Augustus Graeber, and Deider Berheim appealed for acceptance of this group of 39 Lutherans as a new church. At a special session Aug. 14, 1869, at Salem Lutheran Church in Rowan County, the church was received.</p>

						<p>In February 1874, the N.C. Railroad deeded the church 6¾ acres across the tracks. In 1879, the congregation built its first sanctuary, a 36- by 22-foot building. The congregation turned the building in 1895 to face Front Street, and purchased adjoining property.</p>

						<p>Growth spurred planning for a new sanctuary, and in 1909 the congregation moved into the new building, much of which was built by its own members. The first Vacation Bible School was held in 1931.The education building was finished in 1954 and is still in use. All activities moved there in 1962 as the sanctuary deteriorated.</p>

						<p>The present sanctuary was complete by February 1965. It features a stone reredos and an 18-foot cross. The 500-pound bronze bell, struck in 1874, might be the oldest church bell in use in Burlington.Over the years the church has helped community groups and organizations get started, such as the Celebration Singers, Friendship Center Adult Day Care, Allied Churches of Alamance County and Twin Lakes Community. Boy Scout Troop No. 39, chartered at the church in 1928, is one of the oldest Scout troops in the state.</p>

						The celebration continues this Sunday, Sept. 22 — homecoming — when Bishop Tim Smith of the N.C. Synod, ELCA, will preach at the 10 a.m. worship service. Festivities will conclude Oct. 20 with a percussion and organ concert at 5 p.m.</p>

